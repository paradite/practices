/**
 * @param {number[]} hours
 * @return {number}
 */
var longestWPI = function(hours) {
  // console.log(hours.length);
  const memo = {};
  let ans = 0;
  // init first layer
  for (var i = 0; i < hours.length; i++) {
    if (hours[i] > 8) {
      ans = 1;
      memo[i + '-' + i] = [1, 0];
    } else {
      memo[i + '-' + i] = [0, 1];
    }
  }
  for (var i = 2; i <= hours.length; i++) {
    let currentSubLength = i;
    for (var j = 0; j < hours.length - i + 1; j++) {
      let lastLayerLength = currentSubLength - 1;
      let left = memo[j + '-' + (j + lastLayerLength - 1)];
      let end = hours[j + currentSubLength - 1] > 8 ? 1 : 0;
      // build next layer
      let next;
      if (end === 1) {
        next = [left[0] + 1, left[1]];
      } else {
        next = [left[0], left[1] + 1];
      }
      if (next[0] + next[1] > ans && next[0] > next[1]) {
        ans = next[0] + next[1];
      }
      memo[j + '-' + (j + currentSubLength - 1)] = next;
    }
  }
  // console.log(memo);
  return ans;
};

