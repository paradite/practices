/**
 * @param {number} left
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function (left, right) {
  // console.log('TCL ~ this.map', this.map);
  let sum = 0;
  const startingRange = left - (left % this.range10) + this.range10;
  for (let i = left; i <= right; i++) {
    // console.log('TCL ~ i', i);
    if (
      i % this.range10000 === 0 &&
      i + this.range10000 - 1 < right &&
      i >= startingRange &&
      this.map10000[i] !== undefined
    ) {
      sum += this.map10000[i];
      // console.log('TCL ~ this.map[i]', i, this.map[i]);
      i += this.range10000 - 1;
    } else if (
      i % this.range1000 === 0 &&
      i + this.range1000 - 1 < right &&
      i >= startingRange &&
      this.map1000[i] !== undefined
    ) {
      sum += this.map1000[i];
      // console.log('TCL ~ this.map[i]', i, this.map[i]);
      i += this.range1000 - 1;
    } else if (
      i % this.range100 === 0 &&
      i + this.range100 - 1 < right &&
      i >= startingRange &&
      this.map100[i] !== undefined
    ) {
      sum += this.map100[i];
      // console.log('TCL ~ this.map[i]', i, this.map[i]);
      i += this.range100 - 1;
    } else if (
      i % this.range10 === 0 &&
      i + this.range10 - 1 < right &&
      i >= startingRange &&
      this.map10[i] !== undefined
    ) {
      sum += this.map10[i];
      // console.log('TCL ~ this.map[i]', i, this.map[i]);
      i += this.range10 - 1;
    } else {
      sum += this.arr[i];
    }
    // console.log('TCL ~ rangeSum', i, rangeSum);
    // console.log('TCL ~ sum', i, sum);
  }
  return sum;
};

